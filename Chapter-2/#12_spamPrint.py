# A program that prints "Hello" if 1 is given as input. Prints "Howdy" for 2 and prints "Greetings!" for giving any other integers as input.
# Practice questions #9 of this chapter-2

print("Enter input:")
spam=int(input())

if spam==1:
    print("Hello")
elif spam==2:
    print("Howdy")
else:
    print("Greetings!")


